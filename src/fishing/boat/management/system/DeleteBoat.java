    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(null, "Do you really want to delete this boat?", "Select", JOptionPane.YES_NO_CANCEL_OPTION);
    if (confirm == 0) {
        String id = jTextField1.getText().trim(); // Trim any leading or trailing spaces
        System.out.println("Deleting boat with ID: " + id); // Debugging output
        Conn con = new Conn();
        try {
            Connection c = con.c;
            Statement st = c.createStatement();
            int rowsAffected = st.executeUpdate("DELETE FROM boat WHERE Bid='" + id + "'");
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Successfully Deleted.");
                setVisible(false);
                new BoatDetails().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Failed to delete boat. No matching ID found.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error deleting user: " + e.getMessage());
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

